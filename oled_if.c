void 
OLEDInit()
{
    MAP_SPIConfigSetExpClk(OLED,
    		MAP_PRCMPeripheralClockGet(OLED_PERIPH),
    		SPI_BIT_RATE,
    		SPI_MODE_MASTER,
    		SPI_SUB_MODE_0,
    		SPI_4PIN_MODE | SPI_TURBO_OFF | SPI_CS_ACTIVELOW | SPI_SW_CTRL_CS | SPI_WL_8
    		);
    MAP_SPIEnable(OLED);

    //MAP_SPIFIFODisable(OLED, SPI_RX_FIFO);
    GPIO_IF_GetPortNPin(GPIO_RST,&g_uiRSTPort,&g_ucRSTPin);
    GPIO_IF_GetPortNPin(GPIO_DC,&g_uiDCPort,&g_ucDCPin);

    GPIO_IF_Set(GPIO_RST,g_uiRSTPort,g_ucRSTPin, 0);
    MAP_UtilsDelay(1000000);
    GPIO_IF_Set(GPIO_RST,g_uiRSTPort,g_ucRSTPin, 1);

    OLED_SetDisplayOnOff  (0x00);		    // Display Off (0x00/0x01)
    OLED_SetDisplayClock   (0x80);		    // Set Clock as 100 Frames/Sec
    OLED_SetMultiplexRatio (0x3F);		    // 1/64 Duty (0x0F~0x3F)
    OLED_SetDisplayOffset  (0x00);		    // Shift Mapping RAM Counter (0x00~0x3F)
    OLED_SetStartLine        (0x00);			// Set Mapping RAM Display Start Line (0x00~0x3F)
    OLED_SetChargePump     (0x04);		    // Enable Embedded DC/DC Converter (0x00/0x04)
    OLED_SetAddressingMode   (0x02);		    // Set Page Addressing Mode (0x00/0x01/0x02)
    OLED_SetSegmentRemap   (0x01);		    // Set SEG/Column Mapping     0x00左右反置 0x01正常
    OLED_SetCommonRemap    (0x08);		    // Set COM/Row Scan Direction 0x00上下反置 0x08正常
    OLED_SetCommonConfig   (0x10);		    // Set Sequential Configuration (0x00/0x10)
    OLED_SetContrastControl  (BRIGHTNESS);	// Set SEG Output Current
    OLED_SetPrechargePeriod(0xF1);	    	// Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
    OLED_SetVCOMH           (0x40);			// Set VCOM Deselect Level
    OLED_SetEntireDisplay  (0x00);		    // Disable Entire Display On (0x00/0x01)
    OLED_SetInverseDisplay (0x00);		    // Disable Inverse Display On (0x00/0x01)
    OLED_SetDisplayOnOff  (0x01);		    // Display On (0x00/0x01)
    OLED_Fill            (0x00);         //初始清屏
	OLED_SetPos         (0, 0);
}

void OLED_WrDat(byte data)
{
	MAP_SPICSEnable(OLED);
	GPIO_IF_Set(GPIO_DC,g_uiDCPort,g_ucDCPin,1);
    MAP_SPIDataPut(OLED,data);
    MAP_SPIDataGet(OLED, &ulDummy);
    MAP_SPICSDisable(OLED);
}

void OLED_WrCmd(byte cmd)
{
	MAP_SPICSEnable(OLED);
	GPIO_IF_Set(GPIO_DC,g_uiDCPort,g_ucDCPin,0);
    MAP_SPIDataPut(OLED,cmd);
    MAP_SPIDataGet(OLED, &ulDummy);
    MAP_SPICSDisable(OLED);
}

void OLED_SetPos(byte x, byte y)
{
    OLED_WrCmd( 0xb0+y );
    OLED_WrCmd((( x & 0xf0 ) >> 4) | 0x10 );
    OLED_WrCmd( ( x & 0x0f ) | 0x01 );
}

void OLED_Fill(byte bmp_data)
{
	byte y,x;
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			OLED_WrDat(bmp_data);
	}
}

void OLED_CLS(void)
{
	byte y,x;
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			OLED_WrDat(0);
	}
}

void OLED_Delayms(int ms)
{
    MAP_UtilsDelay(ms*SYSCLK/3000);
}

