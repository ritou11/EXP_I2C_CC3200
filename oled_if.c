//函数名：OLED_P8x16Str(byte x,byte y,byte *p)
//功能描述：写入一组标准ASCII字符串
//参数：显示的位置（x,y），y为页范围0～7，要显示的字符串
//返回：无
//==============================================================
void OLED_P8x16Str(byte x,byte y,byte ch[])
{
    byte c=0,i=0,j=0;

    while (ch[j]!='\0')
    {
        c =ch[j]-32;
        if(x>120)
        {
            x=0;
            y++;
        }
        OLED_SetPos(x,y);
  	    for(i=0;i<8;i++)
  	        OLED_WrDat(F8X16[c*16+i]);
    	OLED_SetPos(x,y+1);
  	    for(i=0;i<8;i++)
  	        OLED_WrDat(F8X16[c*16+i+8]);
        x+=8;
  	    j++;
    }
}
//输出汉字字符串
void OLED_P16x16Str(byte x,byte y,byte ch[])
{
	byte wm=0,ii = 0;
	word adder;

	while(ch[ii] != '\0')
	{
  	    wm = 0;
  	    adder = 1;
  	    while(F16x16_Idx[wm] > 127)
  	    {
  		    if(F16x16_Idx[wm] == ch[ii])
  		    {
  			    if(F16x16_Idx[wm + 1] == ch[ii + 1])
  			    {
  				    adder = wm * 16;
  				    break;
  			    }
  		 }
  		wm += 2;
    }

  	if(x>112)
  	{
  	    x=0;
  	    y+=2;
  	}
  	OLED_SetPos(x , y);
  	if(adder != 1)// 显示汉字
  	{
  		OLED_SetPos(x , y);
  		for(wm = 0;wm < 16;wm++)
  			OLED_WrDat(F16x16[adder++]);

  		OLED_SetPos(x,y + 1);
  		for(wm = 0;wm < 16;wm++)
  			OLED_WrDat(F16x16[adder++]);
  	}
  	else			  //显示空白字符
  	{
  		ii += 1;
        OLED_SetPos(x,y);
  		for(wm = 0;wm < 16;wm++)
  			OLED_WrDat(0);

  		OLED_SetPos(x,y + 1);
  		for(wm = 0;wm < 16;wm++)
  			OLED_WrDat(0);
  	}
  	x += 16;
  	ii += 2;
	}
}
//输出汉字和字符混合字符串
void OLED_Print(byte x, byte y, byte ch[])
{
	byte ch2[3];
	byte i=0;
	while(ch[i] != '\0')
	{
		if(ch[i] > 127)
		{
			ch2[0] = ch[i];
	 		ch2[1] = ch[i + 1];
			ch2[2] = '\0';			    //汉字为两个字节
			OLED_P16x16Str(x , y, ch2);	//显示汉字
			if(x>112){
				x=0;
				y+=2;
			}
			x += 16;
			i += 2;
		}
		else
		{
			ch2[0] = ch[i];
			ch2[1] = '\0';			    //字母占一个字节
			OLED_P8x16Str(x , y , ch2);	//显示字母
			if(x>120){
				x=0;
				y++;
			}
			x += 8;
			i++;
		}
	}
}

